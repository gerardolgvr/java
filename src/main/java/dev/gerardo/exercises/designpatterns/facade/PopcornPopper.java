package dev.gerardo.exercises.designpatterns.facade;

public class PopcornPopper {

    public void on() {
        System.out.println("PopcornPopper is on");
    }

    public void pop() {
        System.out.println("PopcornPopper is popping");
    }

    public void off() {
        System.out.println("PopcornPopper is off");
    }
}
